#!/usr/bin/env node
import { execFileSync } from 'node:child_process'
import { readFileSync, writeFileSync, mkdirSync, existsSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const FRONTEND_DIR = resolve(__dirname, '..')
const REPO_DIR = resolve(FRONTEND_DIR, '..')
const MANUAL_PATH = resolve(FRONTEND_DIR, 'src/data/changelog.manual.json')
const OUTPUT_PATH = resolve(FRONTEND_DIR, 'src/data/changelog.ts')

const HEADER = `// AUTO-GENERATED FILE. Do not edit by hand.
// Generated by frontend/scripts/generate-changelog.mjs from git tags and
// frontend/src/data/changelog.manual.json. Re-run \`pnpm run build\` (or the
// generator directly) to refresh.
`

function git(args) {
  try {
    return execFileSync('git', args, {
      cwd: REPO_DIR,
      encoding: 'utf8',
      stdio: ['ignore', 'pipe', 'pipe'],
    }).trim()
  } catch {
    return ''
  }
}

function loadManual() {
  if (!existsSync(MANUAL_PATH)) return { entries: [] }
  try {
    const raw = JSON.parse(readFileSync(MANUAL_PATH, 'utf8'))
    return { entries: Array.isArray(raw.entries) ? raw.entries : [] }
  } catch (err) {
    console.warn(`[changelog] Could not parse ${MANUAL_PATH}:`, err.message)
    return { entries: [] }
  }
}

function listVersionTags() {
  const out = git(['tag', '--list', 'v*', '--sort=-creatordate'])
  if (!out) return []
  return out
    .split('\n')
    .map((t) => t.trim())
    .filter(Boolean)
}

function tagDate(tag) {
  const out = git(['log', '-1', '--format=%cs', tag])
  return out || ''
}

const SKIP_PATTERNS = [
  /^Merge /i,
  /^Git commit prior to merge/i,
  /^Initial commit$/i,
  /^wip\b/i,
  /^chore\(release\)/i,
  /^bump version/i,
]

function cleanSubject(subject) {
  let s = subject.trim()
  // Strip conventional-commit prefix: "feat:", "fix(scope):", etc.
  s = s.replace(/^(feat|fix|chore|docs|refactor|perf|test|build|ci|style)(\([^)]+\))?!?:\s*/i, '')
  // Strip trailing "(task #N)" / "task #N" annotations.
  s = s.replace(/\s*\(?(?:task\s*)?#\d+\)?\s*$/i, '')
  // Strip leading "Task #N:" prefix.
  s = s.replace(/^Task\s*#\d+:\s*/i, '')
  s = s.trim()
  if (!s) return ''
  // Capitalize first letter, ensure trailing period.
  s = s[0].toUpperCase() + s.slice(1)
  if (!/[.!?]$/.test(s)) s += '.'
  return s
}

function commitsBetween(from, to) {
  const range = from ? `${from}..${to}` : to
  const out = git(['log', range, '--no-merges', '--pretty=%s'])
  if (!out) return []
  const seen = new Set()
  const result = []
  for (const line of out.split('\n')) {
    const subject = line.trim()
    if (!subject) continue
    if (SKIP_PATTERNS.some((re) => re.test(subject))) continue
    const cleaned = cleanSubject(subject)
    if (!cleaned) continue
    const key = cleaned.toLowerCase()
    if (seen.has(key)) continue
    seen.add(key)
    result.push(cleaned)
  }
  return result
}

function buildAutoEntries() {
  const tags = listVersionTags() // newest first
  const entries = []

  // Unreleased: commits since the newest tag (or all commits if no tags).
  // Cap the list so tagless checkouts don't flood the About tab with the
  // entire repo history.
  const UNRELEASED_MAX = 25
  const head = git(['rev-parse', 'HEAD'])
  if (head) {
    const newest = tags[0]
    const unreleased = commitsBetween(newest, 'HEAD')
    if (unreleased.length > 0) {
      const truncated = unreleased.length > UNRELEASED_MAX
      const trimmed = truncated ? unreleased.slice(0, UNRELEASED_MAX) : unreleased
      if (truncated) {
        trimmed.push(`…and ${unreleased.length - UNRELEASED_MAX} earlier change${unreleased.length - UNRELEASED_MAX === 1 ? '' : 's'}.`)
      }
      entries.push({
        version: 'Unreleased',
        date: '',
        highlights: trimmed,
      })
    }
  }

  for (let i = 0; i < tags.length; i++) {
    const tag = tags[i]
    const prev = tags[i + 1] // older tag (list is newest-first)
    const highlights = commitsBetween(prev, tag)
    entries.push({
      version: tag.replace(/^v/, ''),
      date: tagDate(tag),
      highlights,
    })
  }

  return entries
}

function mergeEntries(autoEntries, manualEntries) {
  const manualByVersion = new Map(manualEntries.map((e) => [e.version, e]))
  const seen = new Set()
  const merged = []

  for (const auto of autoEntries) {
    const manual = manualByVersion.get(auto.version)
    seen.add(auto.version)
    if (manual) {
      const highlights = Array.isArray(manual.highlights) && manual.highlights.length > 0
        ? manual.highlights
        : auto.highlights
      const extra = Array.isArray(manual.extraHighlights) ? manual.extraHighlights : []
      merged.push({
        version: auto.version,
        date: manual.date || auto.date,
        highlights: [...highlights, ...extra],
      })
    } else {
      merged.push(auto)
    }
  }

  // Append any curated-only entries (e.g. historical releases predating tags)
  // that were not present in git history. Preserve their declared order.
  for (const manual of manualEntries) {
    if (seen.has(manual.version)) continue
    merged.push({
      version: manual.version,
      date: manual.date || '',
      highlights: Array.isArray(manual.highlights) ? manual.highlights : [],
    })
  }

  // Drop entries that ended up with no highlights.
  return merged.filter((e) => e.highlights.length > 0)
}

function render(entries) {
  const body = JSON.stringify(entries, null, 2)
  return `${HEADER}
export interface ChangelogEntry {
  version: string
  date: string
  highlights: string[]
}

export const CHANGELOG: ChangelogEntry[] = ${body}
`
}

function main() {
  const manual = loadManual()
  const auto = buildAutoEntries()
  const merged = mergeEntries(auto, manual.entries)
  mkdirSync(dirname(OUTPUT_PATH), { recursive: true })
  writeFileSync(OUTPUT_PATH, render(merged))
  console.log(
    `[changelog] Wrote ${merged.length} entr${merged.length === 1 ? 'y' : 'ies'} to ${OUTPUT_PATH}`,
  )
}

main()
